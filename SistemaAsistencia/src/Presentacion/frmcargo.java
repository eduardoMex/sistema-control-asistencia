    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

      if(txtcargo.getText().length()==0){
             JOptionPane.showConfirmDialog(rootPane, "Debes Ingresar un cargo a guardar");
             txtcargo.requestFocus();
             return;
        } 
      
       vcargo dts =new  vcargo();
        fcargo func=new fcargo();
        
        
                
        dts.setCargodescripcion(txtcargo.getText());
       
       if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "El cargo fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
        else if(accion.equals("editar")){
            dts.setIdcargo(Integer.parseInt(txtidcargo.getText()));
            
            if(func.editar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "El cargo fue editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
          

    }//GEN-LAST:event_btnguardarActionPerformed

