    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        
        if(!txtidcargo.getText().equals("")){
            int confirmacion=JOptionPane.showConfirmDialog(rootPane, "Esta seguro de Eliminar el cargo","Confirmar",2);
            
            if(confirmacion==0){
                fcargo func=new fcargo();
                vcargo dts=new vcargo();
                
                dts.setIdcargo(Integer.parseInt(txtidcargo.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }
            
        }

    }//GEN-LAST:event_btneliminarActionPerformed

