    private void tablalistadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMousePressed
        
        if(evt.getClickCount()==2){
            int fila =tablalistado.getSelectedRow();
            String cod;
            String nombre;
            String apellido;
            String grado;
            String seccion;
            
            cod=tablalistado.getValueAt(fila, 0).toString();
            nombre=tablalistado.getValueAt(fila, 1).toString();
            apellido=tablalistado.getValueAt(fila, 2).toString();
            grado=tablalistado.getValueAt(fila, 8).toString();
            seccion=tablalistado.getValueAt(fila, 9).toString();
            
            frmpermiso.txtidalumno.setText(cod);
            frmpermiso.txtnombres.setText(nombre);
            frmpermiso.txtapellidos.setText(apellido);
            
            frmpermiso.txtgrado.setText(grado);
            frmpermiso.txtseccion.setText(seccion);
           
            this.dispose();
        }
    }//GEN-LAST:event_tablalistadoMousePressed

